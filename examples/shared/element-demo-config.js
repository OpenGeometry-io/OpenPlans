export function resolveElementExample(key) {
  const canonicalKey = ELEMENT_EXAMPLE_ALIASES[key] ?? key;
  const definition = CANONICAL_ELEMENT_EXAMPLES[canonicalKey];
  if (!definition) {
    return null;
  }

  return {
    key,
    canonicalKey,
    ...definition,
  };
}
